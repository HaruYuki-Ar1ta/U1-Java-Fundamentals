public class Average2 {
    /*
    Given two numbers, num1 and num2, output the average.
    */
    public static void main(String[] args) {
        double num1 = 13.0 ;
        double num2 = 22.0 ;
        double sum = num1 + num2;
        double average = sum / 2 ;

        System.out.println("Starting with the numbers : " + num1 + " and " + num2);
        System.out.println(" When you add them you get: " + sum);
        System.out.println(" Then you get the average: " + average);
    }



}
